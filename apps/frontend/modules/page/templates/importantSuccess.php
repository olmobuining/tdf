		<h2 class="ringbearer">De Website</h2>
		<p>Alle leden zijn ook verplicht om zich te registreren op de website. Op de website hebben wij namelijk een aantal belangrijke functie die wij gebruiken.<br>
		Via jou eigen "mijn account" pagina kan je karakters toevoegen. ( op dit moment werken het crafting systeem op de website nog niet dus maakt niet uit wat je hierbij invult )<br>
		Deze karakters moeten reflecteren wat jij ingame hebt aan karakters. Op de "mijn account" pagina kan je alle karakters levelen door op de "level up" knop te drukken. </p>
		<br>
		<p>De Kalender zal worden gebruikt om alle evenementen te plannen. <br>
		Op deze evenementen kan je met een van je karakters aanmelden. Hiervoor zijn natuurlijk met sommige evenementen een aantal eisen. <br>
		<strong>Eisen</strong>:</p>
		<h3 class="ringbearer medium">Minimum level- kalender</h3>
		<p>Het kan zijn dat er een minimun level is ingesteld om je te kunnen aanmelden, dan zie je geen karakters die onder dit level zijn. </p>
		<h3 class="ringbearer medium">Maximaal aantal deelnemers- kalender</h3>
		<p>Voor evenementen zoals bijvoorbeeld een Dungeons zal er een maximum worden ingesteld op 5 personen, aangezien er in de instance ook een maximum van 5 personen op staat.
		Wel kun je dan nog aanmelden als je aan de andere eisen voldoet. Als backup word je dan ingesteld, als er genoeg is voor 2 of meer groepen worden deze mogelijk gemaakt.</p>
		<h3 class="ringbearer medium">Toelating - kalender</h3>
		<p>Er zijn 2 types evenementen. Open en gesloten. <br>
		Een <strong>open evenement</strong> staat open voor alle leden en worden gelijk geaccepteerd voor het evenement. <br>
		Een <strong>gesloten evenement</strong>, is ook open voor alle leden. Alleen moet je eerst geaccepteerd worden voor je mag meedoen. Dit accepteren zal 4 uur voor het evenements verzameltijd zijn gebeurd.</p>
		
