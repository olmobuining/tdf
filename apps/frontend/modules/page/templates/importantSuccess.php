		<h2 class="ringbearer">Teamspeak</h2>
		<p>Ingame maken wij veel gebruik van teamspeak. Voor gezelligheid maar ook voor snelle communicatie. Het is niet verplicht om op teamspeak te zijn constant, behalve voor bepaalde evenementen. Het is ook niet verplicht om een microfoon te hebben maar kan erg handig zijn. <br>
		Om verwarring te voorkomen moet je gebruik maken van de zelfde gebruikersnaam als op de website. Daarom moet je op het forum en op de website de zelfde naam gebruiken, deze moet het zelfde zijn als de naam in guild wars. ( de naam zonder de cijfers )<br>
		Als je naar de "mijn account" pagina gaat, zal je rechts op die pagina een uitleg vinden hoe je kunt verbinden met onze teamspeak server.</p>
		<p>Op Teamspeak is het natuurlijk heel makkelijk om te roepen "ik". Maar aangezien niet iedereen elkaars stemmen herkent, vragen wij iedereen ipv "ik", je naam te roepen.</p>
		<p><strong>Voorbeeld(hoe het niet moet):</strong> </p>
		<p><em>Killo: "Wie doet er mee aan WvW?"<br>
		HellHunter: "ik"</em></p>
		<p>( nu is killo geforceerd te vragen "wie is ik?" (als je de stem niet herkent) )</p>
		<br>
		<p><strong>Voorbeeld hoe het moet zijn:</strong></p>
		<p>Killo: "Wie doet er mee aan WvW?"<br>
		HellHunter: "Hunter of Nature"</p>
		<p>Let op, hier gebruikt HellHunter niet zijn nickname, maar zijn ingame naam waar hij op dat moment mee speelt. Hierdoor kan hij makkelijk die naam opzoeken</p>
		
