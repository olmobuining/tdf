		<h2 class="ringbearer">Guild structuur</h2>
		<p>Als Guild heb je natuurlijk een duidelijke structuur nodig, vooral als het een wat grotere Guild betreft. Daarom hebben wij een duidelijke structuur voor onze Guild neergezet.</p>
		<p><strong>Leiders/Oprichters</strong>: Killo & HellHunter
		HellHunter en ik (Killo) zijn de oprichters van de Guild. Als leiders van de Guild zorgen wij dat de Guild blijft bestaan, wij hakken de knoop door als het nodig is en bepalen de lastige gingen die je niet democratisch kunt/wilt bepalen.</p>
		<p><strong>Officieren</strong>:
		Officieren hebben een speciale functie in de Guild. Zij hebben een bepaalde taak gekregen om te doen in de Guild. Hierin zullen ze zich natuurlijk moeten bewijzen. Officieren geven net een beetje meer voor de Guild dan een "gewoon" lid. Taken die je van een officier kunt verwachten zijn bijvoorbeeld: WvW evenementenorganisator, Werving, Content check, volledige Professionkennis, enz.</p>
		<p><strong>Leden</strong>:
		Je wordt een lid wanneer je na de proefperiode wordt aangenomen. Alle leden worden voor een voorkeur ingedeeld. Deze voorkeur bepaalt niet wat je moet spelen, het geeft alleen aan wat je liever speelt.</p>
		<p><strong>Stagairs</strong>:
		Stagairs zijn nieuwe leden in onze Guild. Deze worden na ongeveer een maand ( na het registreren op de website ) beoordeeld. Als uit deze beoordeling komt dat de desbetreffende persoon geschikt is voor de Guild wordt hij/zij aangenomen als volledig lid van de Guild. </p>
