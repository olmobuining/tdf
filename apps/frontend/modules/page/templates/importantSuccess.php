		<h2 class="ringbearer">Ons forum</h2>
		<p>Ons forum is het communicatie middel voor ons om van alles te bespreken als wij niet op teamspeak zitten. Hier vind je veel belangrijke informatie. Daarom is het verplicht om je te registreren op het forum. </p>
		<p>Om ons forum zo duidelijk mogelijk te houden is er een archief plek waar alle verouderede berichten verplaatst naar zullen worden. Hier kan dan niet meer op gereageerd worden maar nog wel gelezen. Ook als je vind dat een topic van jou gesloten kan worden kan je dit melden dan sluit ik hem en zal ik het na een tijdje verplaatsen naar het archief. </p>
