		<h3 class="ringbearer medium">Intro</h3>
		<p>Als Guild willen wij veel kunnen bereiken op onze server. Vandaar dat we samen met een aantal andere Guilds een Alliance zijn begonnen. Dit zal betekenen dat we met een aantal WvW evenementen samen zullen werken, om zo veel mogelijk te kunnen bereiken. De Alliance heet: GW2NL. Genaamd naar de website gw2nl.nl, aangezien we via dit platvorm kunnen communiceren.</p>
		<h3 class="ringbearer medium">De Alliance forms</h3>
		<p>Natuurlijk zullen wij onze eigen forums blijven gebruiken. De Alliance forums zijn bedoeld om WvW tactieken te bespreken en evenementen te organiseren. Er zullen ook een aantal andere functies op gw2nl.nl zijn waar je gebruik van kunt maken. <br>
		Onze leiders hebben op dit forum een eigen gedeelte om zo zaken van de Alliance te kunnen bespreken. Heb je zelf idee&euml;n voor de Alliance laat het dan weten op ons forum, dan kunnen de leiders dit ter sprake brengen in de Alliance.<br>
		Het is niet verplicht om te registreren op het forum van de Alliance. Wel zou ik het aanraden als je meewilt praten met Alliance leden en tactieken wilt bespreken/delen.</p>
		<p>Er zal een GW2NL Guild komen voor als wij met de Alliance spelen. In deze Guild worden alle upgrades gekocht voor WvW. De bedoeling van deze Guild is dat wij zoveel mogelijk Alliance leden in deze Guild hebben tijdens WvW om zo herkenbaar over te komen, maar zodra er geen WvW gedaan wordt met de Alliance vragen wij om je eigen Guild te presenteren.</p>
