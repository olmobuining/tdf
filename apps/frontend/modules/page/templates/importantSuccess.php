		<h2 class="ringbearer">Proef periode</h2>
		<p>Zodra je in de guild komt gaat je proef periode in. In deze proef periode zit je in de rank "Stagair". Deze heeft niet minder rechten dan een "volledig" lid. Het enige verschil is dat deze leden na ongeveer een maand worden beoordeeld door de leiding en andere leden of hij/zij in de guild mag blijven.</p>
		<p>Als je proef periode bijna voorbij is denk dan alvast na waar je ingedeeld wilt worden.</p>
		<p>Je kan in 4 delen worden ingedeeld: PvE, PvP, PvX en WvW.</p>
		<ul>
			<li>PvE = Player verses Environment</li>
			<li>PvP = Player Verses Player</li>
			<li>WvW = World vs World vs World</li>
			<li>PvX = allen hierboven genoemd</li>
		</ul>
		<p>Deze voorkeuren bepaald niet gelijk dat je altijd die voorkeur moet spelen. Je mag altijd iets anders doen. Deze voorkeur geeft alleen aan waar jij het meeste te vinden bent waarschijnlijk of wat jij het liefste zou willen doen. </p>
		<p><strong>Een voorbeeld</strong>: Ik speel graag PvE, vooral om te levelen en skills te krijgen, maar als je vraagt aan mij wat speel je liever zou ik PvP zeggen, omdat ik van de competitie hou en daar het meeste ben naast het simpelweg levelen. Dus mijn voorkeur zou zijn PvP.</p>
		<p><strong>Een ander voorbeeld</strong>: Ik speel graag WvW met de alliance of guild en ga af en toe alleen met een groep mee, maar daarnaast speel ik veel PvE. In PvE ben ik vooral bezig alle gebieden 100% te krijgen, alle dungeons in explorable versie en de mooiste armor te verzamelen. Dus mijn voorkeur is PvE.</p>
		<p>Als wij jou hebben geaccepteerd als "volledig" lid of niet, zullen wij contact met je opnemen over je keuze of wij hebben nog een aantal vragen.<br>
		Hiervoor hoef je geen zorgen te maken, Wij verwachten dat elk lid zich volwassen gedraagt en geen andere mensen lastig valt.</p>
		<br>
		<h3 class="ringbearer medium">Waar kijken wij naar in je proef periode?</h3>
		<p>Wij kijken of je in de guild/groep past. Natuurlijk begrijpen wij dat je misschien in 1 maand niet iedereen kan leren kennen, of juist weinig online bent. Dit maakt natuurlijk niet uit. Wij verwachten van elk lid volwassen gedrag.</p>
		<p>Als niet bij de guild past zullen wij een gesprek met jou hebben om te kijken hoe jij je er dan over voelt en kijken of je misschien beter voelt in een andere guild in de alliance. <br>
		Als je een van de regels overtreed zullen wij jou een waarschuwing geven, zodat je weet dat je gedrag of andere dingen niet gewenst zijn. Dit ligt natuurlijk allemaal af van de ernst van de zaak.<br>
		Het zal weinig gebeuren dat een lid niet wordt toegestaan. </p>
